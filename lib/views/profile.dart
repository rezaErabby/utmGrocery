  void createData() async {
    if (_formKey.currentState.validate()) {
      _formKey.currentState.save();
      DocumentReference ref = await db.collection('utmgroceryclc').add({
        'name': '$name',
        'address': '$address',
        'mobile': '$mobile',
        'pin': '$pin',
        'state': '$state'
      });
      setState(() => id = ref.documentID);
      print(ref.documentID);
    }
  }

  void updateData(DocumentSnapshot doc) async {
    await db.collection('utmgroceryclc').document(doc.documentID);
  }

  void deleteData(DocumentSnapshot doc) async {
    await db.collection('utmgroceryclc').document(doc.documentID).delete();
    setState(() => id = null);
  }

  void readData() async {
    DocumentSnapshot snapshot =
        await db.collection('profileinfo').document(id).get();
    print(snapshot.data['name']);
  }

